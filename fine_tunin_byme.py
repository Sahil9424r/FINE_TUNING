# üß© 4. Format your dataset

# We need to make text (input) and labels (target):

def format_translation(example):
    return {
        "text": f"translate English to Hindi: {example['english_text']}",
        "labels": example["hindi_text"]
    }

formatted_dataset = dataset.map(format_translation)

# üî† 5. Tokenize
model_name = "google/mt5-small"

tokenizer = AutoTokenizer.from_pretrained(model_name, use_fast=True)

def tokenize_fn(example):
    inputs = tokenizer(example["text"], truncation=True, padding="max_length", max_length=128)
    targets = tokenizer(example["labels"], truncation=True, padding="max_length", max_length=128)
    inputs["labels"] = targets["input_ids"]
    return inputs

tokenized_datasets = formatted_dataset.map(tokenize_fn, batched=True)

# üöÄ 6. Fine-tuning with LoRA
from peft import LoraConfig, get_peft_model
from transformers import Seq2SeqTrainer

model = AutoModelForSeq2SeqLM.from_pretrained(model_name)

# üîπ LoRA configuration
lora_config = LoraConfig(
    r=16,                # rank
    lora_alpha=32,
    target_modules=["q", "v"],  # attention projections in transformer layers
    lora_dropout=0.05,
    bias="none",
    task_type="SEQ_2_SEQ_LM",
)

model = get_peft_model(model, lora_config)

training_args = TrainingArguments(
    output_dir="./mt5-lora-english-hindi",
    num_train_epochs=1,
    per_device_train_batch_size=1,
    learning_rate=2e-4,
    logging_steps=10,
    save_strategy="epoch",
    fp16=True,
)

trainer = Seq2SeqTrainer(
    model=model,
    args=training_args,
    train_dataset=tokenized_datasets["train"],
)

trainer.train()

